#un programa que lea un entero positivo, n , introducido por el ususario y despues muestre en pantalla la suma de todos los enteros desde 1 hasta n. la suma de los n primero enteros positivos puede ser calculada de la suiguiente forma , suma=n (n+1)/2

n= int(input("escribe un entero positivo "))

suma=n*(n+1)/2

print(f"la suma de 1 hasta {n} es igual a {suma}")